package Fundamental;
public class AssignmentOperators
{
	public static void main(String [] args)
	{
		int a=10;
		a+=10;
		System.out.println(a);
		a*=10;
		System.out.println(a);
		a-=10;
		System.out.println(a);
		a/=9;
		System.out.println(a);
		a^=10;
		System.out.println(a);
		a>>=1;
		System.out.println(a);
		a<<=1;
		System.out.println(a);
	}
}
